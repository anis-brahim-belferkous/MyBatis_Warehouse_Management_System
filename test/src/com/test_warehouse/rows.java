package com.test_warehouse;

public class rows {
    private int id;
    private String name;

    public rows(int id, String name) {
        this.id = id;
        this.name = name;
    }

    // getters and setters for all attributes

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}

